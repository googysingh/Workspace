package loopingstatement;

public class P6 {
// Multiplication table
	public static void main(String[] args) {
		System.out.println("Main Started");
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=10;j++)
			{
				System.out.println(i+"*"+j+"="+i*j);
			}
			System.out.println("************");
		}

	}

}
